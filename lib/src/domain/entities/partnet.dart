class Partner {
  final String id;
  final String name;
  final String address;
  final String phone;
  final String numberPhone;
  final String gender;
  final String email;
  final String state;
  final String statePartner;
  final String action;

  Partner({
    required this.id,
    required this.action,
    required this.email,
    required this.phone,
    required this.numberPhone,
    required this.gender,
    required this.address,
    required this.name,
    required this.state,
    required this.statePartner,
  });
}
